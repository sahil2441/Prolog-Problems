total_fun(Max):-
	cap(Cap),
	findall(Fun,fun(1,Cap,Fun),List),
	max_member(Max,List),
	output(Max).

fun(N,_,0):-
	num(Last),
	N > Last,
	!.

fun(N,Amount,Fun):-
	fun(N,F),
	(
		F > 0
		->
			between(1,Amount,I),
			Fun1 is I*F,
			A4 is Amount-I,
			refill(A4,A2)
		;
			Fun1 is F,
			A3 is Amount-1,
			refill(A3,A2)	
	),
	N1 is N+1,
	fun(N1,A2,Fun2),
	Fun is Fun1+Fun2.

refill(A,A2):-
	refill(R),
	cap(C),
	A3 is A+R,
	(
		A3 <C
		->
			A2 is A3
		;
			A2 is C
	).

output(Max):-
	write(Max).






